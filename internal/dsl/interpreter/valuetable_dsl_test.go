package interpreter_test

import "testing"

// Сквозной DSL-сценарий: Новый ТаблицаЗначений → колонки → строки → Итог.
func TestVT_DSL_BuildAndTotal(t *testing.T) {
	src := `Функция Тест()
  Т = Новый ТаблицаЗначений;
  Т.Колонки.Добавить("Товар");
  Т.Колонки.Добавить("Сумма");
  Стр = Т.Добавить();
  Стр.Товар = "Стул";
  Стр.Сумма = 100;
  Стр2 = Т.Добавить();
  Стр2.Товар = "Стол";
  Стр2.Сумма = 250;
  Возврат Т.Итог("Сумма");
КонецФункции`
	if got := runFunc(t, src); got != float64(350) {
		t.Fatalf("Итог через DSL: ожидалось 350, got %v", got)
	}
}

// Итерация «Для Каждого» по ТаблицеЗначений с чтением колонки строки.
func TestVT_DSL_ForEach(t *testing.T) {
	src := `Функция Тест()
  Т = Новый ТаблицаЗначений;
  Т.Колонки.Добавить("Сумма");
  Стр = Т.Добавить(); Стр.Сумма = 10;
  Стр = Т.Добавить(); Стр.Сумма = 32;
  с = 0;
  Для Каждого Строка Из Т Цикл
    с = с + Строка.Сумма;
  КонецЦикла;
  Возврат с;
КонецФункции`
	if got := runFunc(t, src); !numEq(got, 42) {
		t.Fatalf("сумма через Для Каждого: ожидалось 42, got %v", got)
	}
}
